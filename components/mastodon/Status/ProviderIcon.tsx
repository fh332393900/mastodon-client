function detectProvider(url: string, providerName?: string | null) {
  const name = (providerName ?? "").toLowerCase()
  try {
    const host = new URL(url).hostname.replace("www.", "")
    const combined = `${host} ${name}`

    if (/youtube\.com|youtu\.be/.test(host) || name.includes("youtube")) return "youtube"
    if (/instagram\.com/.test(host) || name.includes("instagram")) return "instagram"
    if (/twitter\.com|x\.com/.test(host) || name === "x" || name.includes("twitter")) return "twitter"
    if (/facebook\.com|fb\.com/.test(host) || name.includes("facebook")) return "facebook"
    if (/reddit\.com/.test(host) || name.includes("reddit")) return "reddit"
    if (/tiktok\.com/.test(host) || name.includes("tiktok")) return "tiktok"
    if (/linkedin\.com/.test(host) || name.includes("linkedin")) return "linkedin"
    if (/twitch\.tv/.test(host) || name.includes("twitch")) return "twitch"
    if (/github\.com/.test(host) || name.includes("github")) return "github"
    if (/bilibili\.com|b23\.tv/.test(host) || name.includes("bilibili")) return "bilibili"
    if (/spotify\.com/.test(host) || name.includes("spotify")) return "spotify"
    if (/vimeo\.com/.test(host) || name.includes("vimeo")) return "vimeo"
    if (/mastodon\.|mstdn\.|social\./.test(host)) return "mastodon"
  } catch {}
  return "default"
}

